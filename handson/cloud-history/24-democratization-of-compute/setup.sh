# ================================================================
# 演習1: ワークロード特性分析シート
# ================================================================
echo ">>> 演習1: ワークロード特性分析シートを作成します..."

cat > workload-analysis.sh << 'SCRIPT1'
#!/bin/bash
set -euo pipefail

echo "═══════════════════════════════════════════════════════════════"
echo "  ワークロード特性分析ツール"
echo "═══════════════════════════════════════════════════════════════"
echo ""
echo "以下の各項目について、1-5のスケールで評価してください。"
echo ""

read -p "プロジェクト名: " PROJECT_NAME

echo ""
echo "【トラフィック変動性】"
echo "  1: 完全に一定（日次/月次の変動 < 10%）"
echo "  2: 軽微な変動（日次の変動 10-30%）"
echo "  3: 中程度の変動（日次/週次で 30-100%）"
echo "  4: 大きな変動（イベント時に 2-5倍）"
echo "  5: 極めて不安定（バーストで 10倍以上）"
read -p "スコア (1-5): " TRAFFIC

echo ""
echo "【成長予測の確実性】"
echo "  1: 5年先まで正確に予測できる"
echo "  2: 2-3年先まで概ね予測できる"
echo "  3: 1年先まで予測できる"
echo "  4: 半年先も不確実"
echo "  5: まったく予測できない"
read -p "スコア (1-5): " GROWTH

echo ""
echo "【レイテンシ要件】"
echo "  1: 秒単位で許容（バッチ処理等）"
echo "  2: 数百ミリ秒（一般的なWebアプリ）"
echo "  3: 数十ミリ秒（リアルタイム性が重要）"
echo "  4: 数ミリ秒（ゲーム、金融取引）"
echo "  5: サブミリ秒（HFT、制御システム）"
read -p "スコア (1-5): " LATENCY

echo ""
echo "【データ主権要件】"
echo "  1: 制約なし"
echo "  2: 国内推奨（ベストエフォート）"
echo "  3: 国内必須（法的要件あり）"
echo "  4: 特定施設必須（規制産業）"
echo "  5: オンプレミス必須（機密データ）"
read -p "スコア (1-5): " SOVEREIGNTY

echo ""
echo "【運用チームの規模・能力】"
echo "  1: 専任インフラチーム 10名以上"
echo "  2: 専任チーム 3-9名"
echo "  3: 兼任チーム 1-3名"
echo "  4: 開発チームが兼務"
echo "  5: インフラ専門家なし"
read -p "スコア (1-5): " TEAM

echo ""
echo "═══════════════════════════════════════════════════════════════"
echo "  分析結果: ${PROJECT_NAME}"
echo "═══════════════════════════════════════════════════════════════"
echo ""

# 弾力性スコア（トラフィック変動性 + 成長予測の不確実性）
ELASTICITY_NEED=$((TRAFFIC + GROWTH))

# マネージド度の必要性（チーム能力が低いほど高い）
MANAGED_NEED=${TEAM}

echo "  トラフィック変動性:   ${TRAFFIC}/5"
echo "  成長予測の不確実性:   ${GROWTH}/5"
echo "  レイテンシ要件:       ${LATENCY}/5"
echo "  データ主権要件:       ${SOVEREIGNTY}/5"
echo "  運用チーム制約:       ${TEAM}/5"
echo ""
echo "  弾力性ニーズ:         ${ELASTICITY_NEED}/10"
echo "  マネージドニーズ:     ${MANAGED_NEED}/5"
echo ""

echo "--- 推奨方向性 ---"
echo ""

if [ "${SOVEREIGNTY}" -ge 4 ]; then
    echo "  [データ主権] オンプレミスまたはソブリンクラウドの検討を推奨"
    echo "               （データ主権要件が高いため）"
    echo ""
fi

if [ "${ELASTICITY_NEED}" -ge 7 ]; then
    echo "  [弾力性] サーバーレス/コンテナオーケストレーションを推奨"
    echo "           （トラフィック変動性と成長不確実性が高い）"
elif [ "${ELASTICITY_NEED}" -le 3 ]; then
    echo "  [弾力性] オンプレミス/リザーブドインスタンスの検討を推奨"
    echo "           （ワークロードが安定的で予測可能）"
else
    echo "  [弾力性] IaaS（Auto Scaling付き）が適合"
    echo "           （中程度の変動に対応）"
fi
echo ""

if [ "${MANAGED_NEED}" -ge 4 ]; then
    echo "  [運用] マネージドサービス/PaaS/サーバーレスを強く推奨"
    echo "         （運用能力の制約が大きい）"
elif [ "${MANAGED_NEED}" -le 2 ]; then
    echo "  [運用] K8s自前運用やベアメタルも選択肢に入る"
    echo "         （十分な運用チームがある）"
fi
echo ""

if [ "${LATENCY}" -ge 4 ]; then
    echo "  [レイテンシ] エッジコンピューティングまたは専用インフラを検討"
    echo "               （厳しいレイテンシ要件）"
fi

echo ""
echo "═══════════════════════════════════════════════════════════════"
echo "  注: この結果は思考の出発点です。最終判断には、"
echo "      コスト分析、既存技術スタック、組織文化などの"
echo "      追加要素を考慮してください。"
echo "═══════════════════════════════════════════════════════════════"

# 結果をファイルに保存
RESULT_FILE="analysis-${PROJECT_NAME// /-}-$(date +%Y%m%d).txt"
{
    echo "プロジェクト: ${PROJECT_NAME}"
    echo "分析日: $(date +%Y-%m-%d)"
    echo "トラフィック変動性: ${TRAFFIC}/5"
    echo "成長予測の不確実性: ${GROWTH}/5"
    echo "レイテンシ要件: ${LATENCY}/5"
    echo "データ主権要件: ${SOVEREIGNTY}/5"
    echo "運用チーム制約: ${TEAM}/5"
    echo "弾力性ニーズ: ${ELASTICITY_NEED}/10"
    echo "マネージドニーズ: ${MANAGED_NEED}/5"
} > "${RESULT_FILE}"

echo "  結果を ${RESULT_FILE} に保存しました。"
SCRIPT1
chmod +x workload-analysis.sh
echo "  -> workload-analysis.sh を作成しました"

