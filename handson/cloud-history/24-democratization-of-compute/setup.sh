#!/bin/bash
set -euo pipefail

echo "═══════════════════════════════════════════════════════════════"
echo "  インフラ選定 意思決定フロー"
echo "═══════════════════════════════════════════════════════════════"
echo ""

read -p "プロジェクト名: " PROJECT

echo ""
echo "Q1: トラフィックの変動性は？"
echo "  a) 極めて不安定（スタートアップ、イベント駆動）"
echo "  b) 安定的・予測可能（成熟事業）"
echo "  c) GPU/AIワークロード中心"
echo "  d) データ主権の規制あり"
read -p "選択 (a/b/c/d): " Q1

case "${Q1}" in
    a)
        echo ""
        echo "  -> 弾力性が最大の価値を持つワークロードです。"
        echo ""
        echo "Q2: チームのインフラ運用能力は？"
        echo "  a) 高い（K8s運用経験あり）"
        echo "  b) 中程度（クラウド経験あり）"
        echo "  c) 低い（インフラ専門家なし）"
        read -p "選択 (a/b/c): " Q2A
        case "${Q2A}" in
            a) RECOMMENDATION="コンテナ/K8s（EKS/GKE等）+ HPA" ;;
            b) RECOMMENDATION="IaaS + Auto Scaling Group" ;;
            c) RECOMMENDATION="サーバーレス（Lambda/Cloud Functions）" ;;
            *) RECOMMENDATION="判定不能" ;;
        esac
        ;;
    b)
        echo ""
        echo "  -> 安定ワークロードです。コスト最適化が鍵です。"
        echo ""
        echo "Q2: 月額クラウド費用は同等オンプレの2倍を超えている？"
        echo "  a) はい"
        echo "  b) いいえ"
        echo "  c) わからない（第23回のTCO分析を実施推奨）"
        read -p "選択 (a/b/c): " Q2B
        case "${Q2B}" in
            a)
                echo ""
                echo "Q3: インフラチームは10名以上？"
                echo "  a) はい"
                echo "  b) いいえ"
                read -p "選択 (a/b): " Q3B
                case "${Q3B}" in
                    a) RECOMMENDATION="オンプレミス or ハイブリッド（第23回参照）" ;;
                    b) RECOMMENDATION="クラウド維持 + RI/Savings Plans最適化" ;;
                    *) RECOMMENDATION="判定不能" ;;
                esac
                ;;
            b) RECOMMENDATION="現行クラウド維持（RI/Savings Plans最適化）" ;;
            c) RECOMMENDATION="まずTCO分析を実施（第23回ハンズオン参照）" ;;
            *) RECOMMENDATION="判定不能" ;;
        esac
        ;;
    c)
        echo ""
        echo "  -> GPU/AIワークロードです。"
        echo ""
        echo "Q2: 連続利用期間の見通しは？"
        echo "  a) 12ヶ月未満（実験・PoC段階）"
        echo "  b) 12ヶ月以上（本番稼働）"
        read -p "選択 (a/b): " Q2C
        case "${Q2C}" in
            a) RECOMMENDATION="クラウドGPUインスタンス（p5/A100等）" ;;
            b) RECOMMENDATION="オンプレミスGPU検討（TCO比較必須）" ;;
            *) RECOMMENDATION="判定不能" ;;
        esac
        ;;
    d)
        echo ""
        echo "  -> データ主権が制約条件です。"
        echo ""
        echo "Q2: 国内クラウドリージョンで法的要件を充足できる？"
        echo "  a) はい"
        echo "  b) いいえ（ソブリンクラウドまたはオンプレが必要）"
        echo "  c) DORA/GDPR等の出口戦略要件がある"
        read -p "選択 (a/b/c): " Q2D
        case "${Q2D}" in
            a) RECOMMENDATION="クラウド（国内リージョン指定）" ;;
            b) RECOMMENDATION="ソブリンクラウド or オンプレミス" ;;
            c) RECOMMENDATION="マルチクラウド or ハイブリッド" ;;
            *) RECOMMENDATION="判定不能" ;;
        esac
        ;;
    *)
        RECOMMENDATION="判定不能"
        ;;
esac

echo ""
echo "═══════════════════════════════════════════════════════════════"
echo "  推奨インフラ: ${RECOMMENDATION}"
echo "═══════════════════════════════════════════════════════════════"
echo ""
echo "  重要: この結果は思考の出発点であり、最終判断ではありません。"
echo "  演習4の言語化テンプレートを使って、選定理由を文書化してください。"
echo "═══════════════════════════════════════════════════════════════"
SCRIPT3
chmod +x decision-flow.sh
echo "  -> decision-flow.sh を作成しました"

